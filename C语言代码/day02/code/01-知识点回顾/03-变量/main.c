#include <stdio.h>
/*
     保存常量的容器就是变量
 
     整形         int 4字节
     单精度浮点型  float 4字节
     双精度浮点型  double 8字节
     字符         char   1字节
 
     定义变量格式： 数据类型   变量名称;
 
 
 */
int main(int argc, const char * argv[]) {
 
//  定义单精度浮点型
    float height;
    
    height = 1.89f;
    
    char sex;
    
    sex = 'M';
    
    
    return 0;
}


