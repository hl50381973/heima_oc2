    5、封装
      (1)请问什么是封装
         1、类是数据和功能的封装
         2、数据封装
            数据是不让外界直接访问，给提供setter 和 getter
            setter方法：给成员变量赋值的
            1、必须是对象方法，一定以 - 开头
            2、必须没有返回值
            3、命名必须是 set + 成员变量名称去掉_首字母大写
            4、必须有一个参数，并且参数类型与所对应的成员变量的类型一致
            getter方法：读取成员变量的值
            1、必须是对象方法，一定以 - 开头
            2、返回值类型必须和所对应的成员变量类型一致
            3、命名必须成员变量名称去掉下划线
            4、必须是空参方法
      (2)请问为什么要使用封装
         1、提高数据安全性
         2、提高程序的灵活性
         3、提高代码可维护性

      (4)请问如何实现属性封装
         1、为类中的每一个成员变量提供setter 和 getter方法
      (5)请简述getter方法的特点
      (6)请简述setter方法的特点
    6、请简述对象与对象之间有哪些依赖关系
       1、方法形参
       2、方法中的局部变量
    7、什么是hasA关系
       当一个对象拥有另外一个对象的时候，他们之间关系就是hasA关系
    8、什么是参数依赖
       当A对象作为另外B对象的方法形参的时候，那么这个B对象就参数依赖于A对象
    9、什么是方法内部依赖
       当A对象作为B的局部变量的时候，那么B对象就方法内部依赖于A对象
       依赖关系是一种比较短暂的关系，当方法执行完毕这种依赖就不存在了
       关联关系是一种长期的关系，关系比较紧密
